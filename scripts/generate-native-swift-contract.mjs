import { createHash } from "node:crypto";
import { readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const contractPath = resolve("contracts/openapi/juno-native-v1.yaml");
const outputArg = process.argv.find((value) => value.startsWith("--output="));
if (!outputArg) throw new Error("Pass --output=/absolute/path/to/JunoNativeContract.swift");
const outputPath = resolve(outputArg.slice("--output=".length));
const source = await readFile(contractPath, "utf8");
for (const required of ["version: 1.0.0", "operationId: exchangeAuthorizationCode", "operationId: listModels", "operationId: getBootstrap", "operationId: listAccountChanges", "operationId: applyAccountMutation", "/auth/refresh:"]) {
  if (!source.includes(required)) throw new Error(`OpenAPI contract is missing required fragment: ${required}`);
}
const digest = createHash("sha256").update(source).digest("hex");
const swift = `// Generated by scripts/generate-native-swift-contract.mjs. Do not edit.
import Foundation

enum JunoNativeContract {
    static let version = "1.0.0"
    static let digest = "${digest}"
}

struct NativeTokenResponse: Decodable, Sendable {
    struct Device: Decodable, Sendable { var id: String }
    var accessToken: String
    var accessTokenExpiresAt: String
    var refreshToken: String
    var refreshTokenExpiresAt: String
    var deviceSession: Device?
}

struct NativeSessionResponse: Decodable, Sendable { var profile: BackendUser }

struct NativeAPIErrorEnvelope: Decodable, Sendable {
    struct Detail: Decodable, Sendable { var code: String; var message: String; var retryable: Bool? }
    var error: Detail
}

struct BootstrapResponse: Decodable, Sendable {
    var currentChangeCursor: String
    var compactionFloorCursor: String
    var modelManifestVersion: String
}

struct AccountChangeDTO: Decodable, Sendable {
    var cursor: String
    var entityType: String
    var entityId: String
    var parentEntityId: String?
    var revision: Int
    var operation: String
    var changedAt: String
}

struct ChangesResponse: Decodable, Sendable {
    var after: String
    var changes: [AccountChangeDTO]
    var nextCursor: String
    var compactionFloorCursor: String
    var hasMore: Bool
}
`;
await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, swift);
console.log(`Generated ${outputPath} from OpenAPI SHA-256 ${digest}`);
