import { createHash } from "node:crypto";
import { readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const contractPath = resolve("contracts/openapi/juno-native-v1.yaml");
const outputArg = process.argv.find((value) => value.startsWith("--output="));
if (!outputArg) throw new Error("Pass --output=/absolute/path/to/JunoNativeContract.swift");
const outputPath = resolve(outputArg.slice("--output=".length));
const source = await readFile(contractPath, "utf8");
for (const required of ["operationId: exchangeAuthorizationCode", "operationId: listModels", "operationId: getBootstrap", "operationId: listAccountChanges", "operationId: applyAccountMutation", "/auth/refresh:", "com.liammagnier.juno://auth/callback", "juno://auth/callback"]) {
  if (!source.includes(required)) throw new Error(`OpenAPI contract is missing required fragment: ${required}`);
}
const version = source.match(/^  version: ([0-9]+\.[0-9]+\.[0-9]+)$/m)?.[1];
if (!version) throw new Error("OpenAPI contract is missing a semantic info.version");
const digest = createHash("sha256").update(source).digest("hex");
const swift = `// Generated by scripts/generate-native-swift-contract.mjs. Do not edit.
import Foundation

public enum JunoNativeContract {
    public static let version = "${version}"
    public static let digest = "${digest}"
    public static let canonicalRedirectURI = "com.liammagnier.juno://auth/callback"
    public static let acceptedRedirectURIs: Set<String> = [canonicalRedirectURI, "juno://auth/callback"]
}

public struct NativeDeviceSessionDTO: Decodable, Sendable {
    public var id: String
    public var name: String
    public var platform: String?
    public var appVersion: String?
    public var createdAt: String
    public var lastSeenAt: String?
    public var revokedAt: String?
    public var current: Bool?
}

public struct NativeTokenResponse: Decodable, Sendable {
    public var tokenType: String
    public var accessToken: String
    public var accessTokenExpiresAt: String
    public var refreshToken: String
    public var refreshTokenExpiresAt: String
    public var deviceSession: NativeDeviceSessionDTO?
}

public struct NativeProfileDTO: Decodable, Sendable {
    public var id: String
    public var name: String?
    public var email: String
    public var image: String?
}

public struct NativeSessionResponse: Decodable, Sendable {
    public var profile: NativeProfileDTO
    public var deviceSession: NativeDeviceSessionDTO
    public var accessTokenExpiresAt: String
    public var contractVersion: String
    public var minimumSupportedAppVersion: String
}

public struct NativeAPIErrorEnvelope: Decodable, Sendable {
    public struct Detail: Decodable, Sendable {
        public var code: String
        public var message: String
        public var requestId: String
        public var retryable: Bool
        public var retryAfterMs: Int?
    }
    public var error: Detail
}

public struct BootstrapResponse: Decodable, Sendable {
    public var currentChangeCursor: String
    public var compactionFloorCursor: String
    public var modelManifestVersion: String
}

public struct AccountChangeDTO: Decodable, Sendable {
    public var cursor: String
    public var entityType: String
    public var entityId: String
    public var parentEntityId: String?
    public var revision: Int
    public var operation: String
    public var changedAt: String
}

public struct ChangesResponse: Decodable, Sendable {
    public var after: String
    public var changes: [AccountChangeDTO]
    public var nextCursor: String
    public var compactionFloorCursor: String
    public var hasMore: Bool
}
`;
await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, swift);
console.log(`Generated ${outputPath} from OpenAPI SHA-256 ${digest}`);
